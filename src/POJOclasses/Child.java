package POJOclasses;

public class Child {

  private String childid;
  private String sid;
  private short age;


  public String getChildid() {
    return childid;
  }

  public void setChildid(String childid) {
    this.childid = childid;
  }


  public String getSid() {
    return sid;
  }

  public void setSid(String sid) {
    this.sid = sid;
  }


  public short getAge() {
    return age;
  }

  public void setAge(short age) {
    this.age = age;
  }

}
